package org.idx.lms.libs;

public class LibsGeneral {

	public String getTrueFalseStringUP(boolean val){
		String ret ="";
		if(val){
			ret = "TRUE";
		}
		else{
			ret = "FALSE";
		}
		return ret;
	}
	
	public String getTrueFalseStringUp(boolean val){
		String ret ="";
		if(val){
			ret = "True";
		}
		else{
			ret = "False";
		}
		return ret;
	}
	
}
