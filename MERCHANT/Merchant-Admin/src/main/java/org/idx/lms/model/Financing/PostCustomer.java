package org.idx.lms.model.Financing;

public class PostCustomer {
	String cst_fname;
	String cst_lname;
	String cst_sex;
	String cst_address;
	String cst_dob;
	String acc_product_code;
	
	public String getCst_fname() {
		return cst_fname;
	}
	public void setCst_fname(String cst_fname) {
		this.cst_fname = cst_fname;
	}
	
	public String getCst_lname() {
		return cst_lname;
	}
	public void setCst_lname(String cst_lname) {
		this.cst_lname = cst_lname;
	}
	
	public String getCst_sex() {
		return cst_sex;
	}
	public void setCst_sex(String cst_sex) {
		this.cst_sex = cst_sex;
	}
	
	public String getCst_address() {
		return cst_address;
	}
	public void setCst_address(String cst_address) {
		this.cst_address = cst_address;
	}
	
	public String getCst_dob() {
		return cst_dob;
	}
	public void setCst_dob(String cst_dob) {
		this.cst_dob = cst_dob;
	}
	
	public String getAcc_product_code() {
		return acc_product_code;
	}
	public void setAcc_product_code(String acc_product_code) {
		this.acc_product_code = acc_product_code;
	}
}
