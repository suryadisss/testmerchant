package org.idx.lms.model.backup;

public class PostBackup {

	public String p_id, p_database, p_exepath, p_host, p_port, p_username, p_password, p_cleanupday, p_ext, p_destfile, p_user;

	public String getP_id() {
		return p_id;
	}

	public void setP_id(String p_id) {
		this.p_id = p_id;
	}

	public String getP_database() {
		return p_database;
	}

	public void setP_database(String p_database) {
		this.p_database = p_database;
	}

	public String getP_exepath() {
		return p_exepath;
	}

	public void setP_exepath(String p_exepath) {
		this.p_exepath = p_exepath;
	}

	public String getP_host() {
		return p_host;
	}

	public void setP_host(String p_host) {
		this.p_host = p_host;
	}

	public String getP_port() {
		return p_port;
	}

	public void setP_port(String p_port) {
		this.p_port = p_port;
	}

	public String getP_username() {
		return p_username;
	}

	public void setP_username(String p_username) {
		this.p_username = p_username;
	}

	public String getP_password() {
		return p_password;
	}

	public void setP_password(String p_password) {
		this.p_password = p_password;
	}

	public String getP_cleanupday() {
		return p_cleanupday;
	}

	public void setP_cleanupday(String p_cleanupday) {
		this.p_cleanupday = p_cleanupday;
	}

	public String getP_ext() {
		return p_ext;
	}

	public void setP_ext(String p_ext) {
		this.p_ext = p_ext;
	}

	public String getP_destfile() {
		return p_destfile;
	}

	public void setP_destfile(String p_destfile) {
		this.p_destfile = p_destfile;
	}

	public String getP_user() {
		return p_user;
	}

	public void setP_user(String p_user) {
		this.p_user = p_user;
	}
	
	
}
