package org.idx.lms.model.ruleset;

public class itemTable {
	String No, Type, Componen, ID;

	public String getNo() {
		return No;
	}

	public void setNo(String no) {
		No = no;
	}

	public String getType() {
		return Type;
	}

	public void setType(String type) {
		Type = type;
	}

	public String getComponen() {
		return Componen;
	}

	public void setComponen(String componen) {
		Componen = componen;
	}

	public String getID() {
		return ID;
	}

	public void setID(String iD) {
		ID = iD;
	}
}
